function Warrior(name) {
  this.name = name;
  this.charClass = "Warrior";
  this.health = 100;
  this.damage = 5;
  this.special = 0;
}
function Wizard(name) {
  this.name = name;
  this.charClass = "Wizard";
  this.health = 70;
  this.damage = 8;
  this.special = 0;
}
function Thief(name) {
  this.name = name;
  this.charClass = "Thief";
  this.health = 80;
  this.damage = 7;
  this.special = 0;
}
